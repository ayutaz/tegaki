// Regenerate `kanji-codepoints.ts` from the davidluzgouveia/kanji-data JSON.
//
// Usage:
//   bun scripts/build-kanji-codepoints.ts
//
// Source: https://raw.githubusercontent.com/davidluzgouveia/kanji-data/master/kanji.json
//
// Grade semantics (per upstream README):
//   1-6 : Kyōiku kanji (elementary school)
//   8   : Jōyō kanji introduced outside elementary (junior/senior high)
//   9,10: Jinmeiyō kanji
//   null: hyōgaigai / out-of-list
//
// "Jōyō" in Japanese education parlance = grade 1-6 + 8 (total 2,136 as of
// the 2010 reform). "Jinmeiyō" = grade 9 + 10 (total 863, with a handful in
// the CJK Compatibility Ideographs block we exclude here because KanjiVG
// has no coverage there).

import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const SOURCE_URL = 'https://raw.githubusercontent.com/davidluzgouveia/kanji-data/master/kanji.json';
const OUT_PATH = resolve(import.meta.dir, 'kanji-codepoints.ts');

interface KanjiEntry {
  grade?: number | null;
}

async function main(): Promise<void> {
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`fetch ${SOURCE_URL}: HTTP ${res.status}`);
  const data = (await res.json()) as Record<string, KanjiEntry>;

  const joyo: number[] = [];
  const jinmei: number[] = [];
  for (const [k, v] of Object.entries(data)) {
    const cp = k.codePointAt(0);
    if (cp === undefined) continue;
    const g = v.grade;
    if (g == null) continue;
    // Filter out CJK Compatibility Ideographs — KanjiVG has no SVGs there.
    if (cp < 0x4e00 || cp > 0x9fff) continue;
    if (g >= 1 && g <= 8 && g !== 7) joyo.push(cp);
    else if (g === 9 || g === 10) jinmei.push(cp);
  }
  joyo.sort((a, b) => a - b);
  jinmei.sort((a, b) => a - b);

  const hex = (cp: number) => `0x${cp.toString(16).padStart(4, '0')}`;
  const fmtArray = (arr: number[], perLine = 12): string => {
    const lines: string[] = [];
    for (let i = 0; i < arr.length; i += perLine) {
      lines.push(
        `  ${arr
          .slice(i, i + perLine)
          .map(hex)
          .join(', ')},`,
      );
    }
    return lines.join('\n');
  };

  const body = `// Auto-generated by scripts/build-kanji-codepoints.ts. Do not edit by hand.
//
// Regenerate with:
//   bun scripts/build-kanji-codepoints.ts
//
// Source: ${SOURCE_URL}
// Scope: CJK Unified Ideographs (U+4E00–U+9FFF) only.

/** Jōyō kanji (常用漢字) — grades 1–6 + 8 per the 2010 MEXT revision. */
export const JOYO_CODEPOINTS: readonly number[] = Object.freeze([
${fmtArray(joyo)}
]);

/** Jinmeiyō kanji (人名用漢字) — grades 9 + 10, CJK Unified only. */
export const JINMEI_CODEPOINTS: readonly number[] = Object.freeze([
${fmtArray(jinmei)}
]);
`;

  writeFileSync(OUT_PATH, body, 'utf-8');
  console.log(`[build-kanji-codepoints] joyo=${joyo.length} jinmei=${jinmei.length} → ${OUT_PATH}`);
}

await main();
